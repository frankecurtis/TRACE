% TRACE: printHeader
function printHeader(T,P)

% Print software name
T.reporter.printf(Enumerations.R_SOLVER,Enumerations.R_BASIC,...
  ['+---------------------------------------------------------+\n'...
  '|                          TRACE                          |\n'...
  '| Trust-Region Algorithm with Contractions and Expansions |\n'...
  '|          Open source code under the MIT License         |\n'...
  '+---------------------------------------------------------+\n'...
  '\n'...
  'This is TRACE version 0.1.0\n'...
  '\n']);

% Print problem information
T.reporter.printf(Enumerations.R_SOLVER,Enumerations.R_BASIC,...
  ['Problem name...................... : %s\n'...
  'Number of variables............... : %d\n'...
  '\n'],...
  P.name,...
  P.numberOfVariables);

% Print header information
T.reporter.printf(Enumerations.R_SOLVER,Enumerations.R_BASIC,...
  ['Subproblem solver................. : %s\n'...
  'Step computation.................. : %s\n'...
  'Acceptability check............... : %s\n'...
  '\n'],...
  T.strategies.stepComputation.name,...
  T.strategies.subproblemSolver.name,...
  T.strategies.acceptabilityCheck.name);

end % printHeader